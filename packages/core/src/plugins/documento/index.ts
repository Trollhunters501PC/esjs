export function replace() {
  return {
    ...replaceObjectStaticMethods({
      from: 'documento',
      to: 'document',
      methods: methods,
    }),
    ...replaceObjectStaticProperties({
      from: 'documento',
      to: 'document',
      properties,
    }),
    ...replaceInstanceof({
      from: 'Documento',
      to: 'Document',
    }),
    ...replaceObjects({
      objects,
    }),
  }
}
